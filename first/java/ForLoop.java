package first.java;

public class ForLoop {

	public static void main(String[] args) {
		int i;
		for( i = 0; i<=2;i++) {
			System.out.println("welcome");
			
		}
		System.out.println(i);
		System.out.println("------------------");
		
		for(int ctr = 0; ctr<=2;ctr++) {
			System.out.println("welcome");
		}
	}

}
