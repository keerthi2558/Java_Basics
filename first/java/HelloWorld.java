package first.java;

public class HelloWorld {

	public static void main(String[] args) {
		int i = 1;
		int j = 2;
		int k = 3;
		int l = 4;
		int m = 5;
		int n = 6;
		int o = 7;
		int p = 8;
		int q = 9;
		int r = 10;
		System.out.print(i);
		System.out.print(j);
		System.out.print(k);
		System.out.print(l);
		System.out.println(m);
		System.out.print(n);
		System.out.print(o);
		System.out.print(p);
		System.out.print(q);
		System.out.print(r);
		System.out.println();
	
		

		System.out.println("printing the i,j using print in same line");
		System.out.print(i);
		System.out.print(j);
		System.out.print(k);

		System.out.println("\nprinting the i,j using print in same line and want to print the k in next line");
		System.out.print(i);
		System.out.print(j);
		System.out.println();
		System.out.print(k);

		System.out.println("\n printing the i using print in frst line and want to print the j,k in next line");
		System.out.print(i);
		System.out.println();
		System.out.print(j);
		System.out.print(k);
		System.out.println();
//		In Java, both print and println are used to display output on the console. 
//		The key difference lies in how they handle the cursor position after printing.
//
//		print: This method prints the output without moving the cursor to the next line. 
//		It keeps the cursor on the same line after printing the content.
//		Example:
//
//		java
//		Copy code
//		System.out.print("Hello, ");
//		System.out.print("world!");
//		// Output: Hello, world! (Both strings are printed on the same line)
//		println: This method prints the output and moves the cursor to the beginning of the next line. It adds a newline character after printing the content.
//		Example:
//
//		java
//		Copy code
//		System.out.println("Hello, ");
//		System.out.println("world!");
//		// Output:
//		// Hello,
//		// world! (Each string is printed on a new line)
//
//
//		if you write System.out.println(); with no arguments (empty parentheses), it simply prints an empty line (a line break) to the console without displaying any content.
//
//		Example:
//
//		java
//		Copy code
//		System.out.println("Hello");
//		System.out.println(); // Prints an empty line
//		System.out.println("World");
//		// Output:
//		// Hello
//		//
//		// World
		
		
	}
	
		
		

}

