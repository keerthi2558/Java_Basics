package first.java;

public class CCFourNestedWhileLoop {

	public static void main(String[] args) {
		int i = 1;
		
		while (i<=7) {
			
			int j= 1;
			while(j<=i) {
				
				System.out.print(i);
				
				j++;
			}
				System.out.println();
			i++;
		}
			
		
		}

	}



